            tip="点击上传资格证书"
            action="file/upload"
            :limit="3"
            :multiple="true"
            :fileUrls="ruleForm.zigezhengshu?ruleForm.zigezhengshu:''"
            @change="zigezhengshuUploadChange"
            ></file-upload>
          </el-form-item>
            <el-form-item :style='{"padding":"10px","margin":"0 0 10px"}' class="upload" v-else label="资格证书" prop="zigezhengshu">
                <img v-if="ruleForm.zigezhengshu.substring(0,4)=='http'" class="upload-img" style="margin-right:20px;" v-bind:key="index" :src="ruleForm.zigezhengshu.split(',')[0]" width="100" height="100">
                <img v-else class="upload-img" style="margin-right:20px;" v-bind:key="index" v-for="(item,index) in ruleForm.zigezhengshu.split(',')" :src="baseUrl+item" width="100" height="100">
